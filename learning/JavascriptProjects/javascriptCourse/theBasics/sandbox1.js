let age = 25

// '===' & '!==' are strict comparison, 
// while '==' & '!=' are loose comparison
console.log('age == 25');
console.log(age == 25);
console.log("age == '25'");
console.log(age == '25');
console.log("age === '25'");
console.log(age === '25');
console.log("age !== '25'");
console.log(age !== '25');
console.log('age !== 25');
console.log(age !== 25);
